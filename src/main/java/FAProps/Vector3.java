package FAProps;

/**
 *
 * @author Basti_Temp
 */
public class Vector3 {

    public float x, y, z;

    public Vector3(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    @Override
    public String toString() {
        return "x: " + x + " | y: " + y + " | z: " + z;
    }
}
